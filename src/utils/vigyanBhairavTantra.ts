export const getTechniquesByCategory = (category: string): VBTTechnique[] => {
  return vigyanBhairavTantraTechniques.filter(technique => technique.category === category);
};

export const getTechniquesByDifficulty = (difficulty: string): VBTTechnique[] => {
  return vigyanBhairavTantraTechniques.filter(technique => technique.difficulty === difficulty);
};

export const getRandomTechnique = (): VBTTechnique => {
  const randomIndex = Math.floor(Math.random() * vigyanBhairavTantraTechniques.length);
  return vigyanBhairavTantraTechniques[randomIndex];
};

export const searchTechniques = (query: string): VBTTechnique[] => {
  const lowercaseQuery = query.toLowerCase();
  return vigyanBhairavTantraTechniques.filter(technique => 
    technique.title.toLowerCase().includes(lowercaseQuery) ||
    technique.description.toLowerCase().includes(lowercaseQuery) ||
    technique.category.toLowerCase().includes(lowercaseQuery)
  );
};